def generate_model_go(fields, ref):
    lines = ["package model", "", "import (", "\t\"time\"", ")", ""]
    # Only import time if needed
    has_time = any(f["go_type"] == "time.Time" for f in fields)
    if not has_time:
        lines = ["package model", "", ""]

    lines.append(f"// {ref.replace('_', ' ').title()} model")
    lines.append("type Object struct {")

    for f in fields:
        gorm_parts = [f"column:{f['column']}"]
        if f.get("is_pk"):
            gorm_parts.append("primaryKey")
        if f.get("is_auto_increment"):
            gorm_parts.append("autoIncrement")
        gorm_str = ";".join(gorm_parts)
        json_str = f["json_name"]
        lines.append(f"\t{f['name']}\t\t{f['go_type']}\t`gorm:\"{gorm_str}\" json:\"{json_str}\"`")

    lines.append("}")
    lines.append("")
    lines.append("func (o *Object) GetID() int64 {")
    lines.append("\treturn o.Id")
    lines.append("}")
    lines.append("")
    return "\n".join(lines)


def generate_db_sh(table_name, fields, category, db_user):
    col_lines = []
    comment_lines = []

    ref_field = "id_basis" if category in ("directory", "document") else "id_document"
    unique_cols = ["id", ref_field] if category != "ledger" else ["id_document", "id_employee"]

    for f in fields:
        nullable = "" if f.get("not_null") else ""
        default = ""
        if f.get("column") == "status":
            default = " default 1"

        pk_part = ""
        if f.get("is_pk") and f.get("is_auto_increment"):
            pk_part = " GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY"
        elif f.get("is_pk"):
            pk_part = " PRIMARY KEY"

        col_lines.append(f"    {f['column']}\t\t{f['sql_type']}{nullable}{default}{pk_part},")

        label = f.get("json_name", f["column"]).replace("_", " ").title()
        comment_lines.append(f"  COMMENT ON COLUMN ${{DB_TABLENAME}}.{f['column']} IS '{label}';")

    cols_str = "\n".join(col_lines)
    comments_str = "\n".join(comment_lines)
    unique_str = f"UNIQUE ({', '.join(unique_cols)})" if unique_cols else ""

    return f'''#!/bin/bash
psql -v ON_ERROR_STOP=1 --username "postgres" --dbname "postgres" <<-EOSQL
  CREATE DATABASE ${{DB_NAME}};
  CREATE USER ${{DB_USER}} WITH PASSWORD '${{DB_PASSWORD}}';
  GRANT ALL PRIVILEGES ON DATABASE ${{DB_NAME}} TO ${{DB_USER}};
EOSQL

psql -v ON_ERROR_STOP=1 --username "postgres" --dbname "${{DB_NAME}}" <<-EOSQL
    GRANT USAGE, CREATE ON SCHEMA public TO ${{DB_USER}};
EOSQL

psql -v ON_ERROR_STOP=1 --username "${{DB_USER}}" --dbname "${{DB_NAME}}" <<-EOSQL
  CREATE TABLE public.${{DB_TABLENAME}} (
{cols_str}
{unique_str}
  );

  COMMENT ON TABLE  ${{DB_TABLENAME}} IS '{table_name.replace("_", " ").title()}';
{comments_str}
EOSQL
'''


def get_services_grouped():
    """Return dict of category -> list of ms_names from go.work + project_reader."""
    try:
        from project_reader import read_all_microservices
        services = read_all_microservices()
    except ImportError:
        services = []
    grouped = {"directory": [], "document": [], "ledger": []}
    for s in services:
        cat = s["category"]
        if cat in grouped:
            grouped[cat].append(s["name"])
    for cat in grouped:
        grouped[cat] = sorted(set(grouped[cat]))
    return grouped


def generate_dockerfile(category, ms_name, all_services=None):
    """Generate Dockerfile that lists ALL microservices (monorepo build pattern)."""
    if all_services is None:
        all_services = get_services_grouped()

    # Ensure the new service is included in its category
    if ms_name not in all_services.get(category, []):
        all_services.setdefault(category, []).append(ms_name)
        all_services[category] = sorted(all_services[category])

    lines = [
        "FROM golang:1.26-alpine AS builder",
        "WORKDIR /app",
        "",
        "# Сначала копируем файлы модулей для кэширования слоев",
        "#",
        "# Копируем go.mod и go.sum ОБОИХ проектов",
    ]

    for cat in ("directory", "document", "ledger"):
        names = all_services.get(cat, [])
        if names:
            if lines and not lines[-1].startswith("#"):
                lines.append("")
            for n in names:
                lines.append(f"COPY ./source/{cat}/{n}/go.mod ./{cat}/{n}/")

    lines.append("")
    lines.append("")
    lines.append("COPY ./source/shared/go.mod ./shared/")
    lines.append("COPY ./source/shared/go.sum ./shared/")
    lines.append("")
    lines.append("COPY ./source/go.work ./")
    lines.append("COPY ./source/go.work.sum ./")
    lines.append("")
    lines.append("# Копируем исходники")

    for cat in ("directory", "document", "ledger"):
        names = all_services.get(cat, [])
        if names:
            lines.append("")
            for n in names:
                lines.append(f"COPY ./source/{cat}/{n}/ ./{cat}/{n}/")

    lines.append("")
    lines.append("")
    lines.append("COPY ./source/shared/ ./shared/")
    lines.append("")
    lines.append(f"WORKDIR /app/{category}/{ms_name}")
    lines.append("# Собираем бинарный файл")
    lines.append("RUN CGO_ENABLED=0 GOOS=linux go build -v -o main .")
    lines.append("")
    lines.append("# Финальный минимальный образ (Distroless или Alpine)")
    lines.append("#FROM alpine:latest")
    lines.append("#WORKDIR /root/")
    lines.append("#COPY --from=builder /app/main .")
    lines.append('#ENTRYPOINT ["./main"]')
    lines.append('CMD ["./main"]')
    lines.append("")

    return "\n".join(lines)


def update_all_dockerfiles(ref, category):
    """Add new service's go.mod and source COPY lines to all existing Dockerfiles."""
    ms_name = f"ms_{ref}"
    dockerfiles_dir = PROJECT_ROOT / "Dockerfiles"
    updated = []
    for df_path in sorted(dockerfiles_dir.iterdir()):
        if not df_path.name.startswith("ms_") or df_path.name == ms_name:
            continue
        if df_path.is_dir():
            continue
        content = df_path.read_text()

        # Check if already has this service
        if f"/{category}/{ms_name}/" in content:
            continue

        go_mod_line = f"COPY ./source/{category}/{ms_name}/go.mod ./{category}/{ms_name}/"
        source_line = f"COPY ./source/{category}/{ms_name}/ ./{category}/{ms_name}/"

        # Find the go.mod section for this category and add the new go.mod line
        # Pattern: after the last go.mod COPY for this category, before the next category
        cat_pattern = f"COPY ./source/{category}/"
        # Find the last line of go.mod copies for this category
        lines = content.split("\n")
        last_go_mod_idx = -1
        last_source_idx = -1
        for i, line in enumerate(lines):
            if line.startswith(f"COPY ./source/{category}/") and line.endswith("/go.mod"):
                last_go_mod_idx = i
            if line.startswith(f"COPY ./source/{category}/") and line.endswith("/"):
                last_source_idx = i

        changed = False
        if last_go_mod_idx >= 0:
            lines.insert(last_go_mod_idx + 1, go_mod_line)
            last_source_idx += 1  # Shift after insert
            changed = True
        if last_source_idx >= 0:
            lines.insert(last_source_idx + 1, source_line)
            changed = True

        if changed:
            df_path.write_text("\n".join(lines) + "\n")
            updated.append(df_path.name)

    return updated


