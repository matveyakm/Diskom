def generate_system_object_insert(name, description, ref, prefix, prefix_number,
                                   level, is_group, id_group, output_order, icon,
                                   table_title, item_title, fields, category="directory"):
    """Generate INSERT SQL for system_object_directory db.sh."""
    table_columns = generate_table_columns_json(fields) if not is_group else ""
    item_fields = generate_item_fields_json(fields, category) if not is_group else ""
    privileges = generate_privileges_json() if not is_group else ""

    cols = [
        "name", "description", "status", "prefix", "prefix_number",
        "level", "is_group", "id_group", "ref", "output_order", "icon",
        "table_columns", "table_title", "item_title", "item_fields",
        "on_save_new", "on_save_edit", "on_acceptance", "on_delete",
        "privileges", "related_documents", "printing_template", "id_transaction"
    ]

    def fmt(val):
        if isinstance(val, bool):
            return "TRUE" if val else "FALSE"
        if isinstance(val, int):
            return str(val)
        s = escape_sql(val)
        if "\n" in s:
            first, rest = s.split("\n", 1)
            rest = "\n".join("    " + l for l in rest.split("\n"))
            s = first + "\n" + rest
        return s

    values = [
        name, description, 1, prefix, prefix_number,
        level, is_group, id_group, ref, output_order, icon,
        table_columns, table_title, item_title, item_fields,
        "", "", "", "",
        privileges, "", "", ""
    ]
    col_str = ",\n    ".join(cols)
    val_str = ",\n    ".join(fmt(v) for v in values)
    return (
        "  insert into ${DB_TABLENAME}\n"
        "  (\n"
        f"    {col_str}\n"
        "  ) values (\n"
        f"    {val_str}\n"
        "  );\n"
    )


SYSTEM_OBJECT_DIR = (
    PROJECT_ROOT / "source" / "directory" / "ms_system_object_directory"
)
SYSTEM_OBJECT_DBSHELL = SYSTEM_OBJECT_DIR / "dbshell"
SEED_DB_PATH = SYSTEM_OBJECT_DBSHELL / "db.sh"
BUILD_DB_SCRIPT = SYSTEM_OBJECT_DIR / "build-db.sh"


def get_system_object_insert_path(ref, category="directory"):
    """Return the path to the per-entity system_object_insert.sql file.

    INSERT files live in each entity's system_description/ folder.
    Groups are stored in ms_system_object_directory/system_description/.
    """
    if ref == "#" or ref is None:
        return SYSTEM_OBJECT_DIR / "system_description"
    return PROJECT_ROOT / "source" / category / f"ms_{ref}" / "system_description" / "01_system_object.sql"


def write_system_object_insert(insert_sql, ref, category="directory"):
    """Write an INSERT to the per-entity system_description/01_system_object.sql file."""
    target_path = get_system_object_insert_path(ref, category)
    target_path.parent.mkdir(parents=True, exist_ok=True)
    target_path.write_text(insert_sql + "\n")
    return True, f"INSERT written to {target_path}"


def regenerate_db_sh():
    """Regenerate db.sh from all per-entity system_object_insert.sql files."""
    if not BUILD_DB_SCRIPT.exists():
        return False, f"build-db.sh not found at {BUILD_DB_SCRIPT}"
    result = subprocess.run(["bash", str(BUILD_DB_SCRIPT)], capture_output=True, text=True)
    if result.returncode != 0:
        return False, f"build-db.sh failed: {result.stderr}"
    return True, f"db.sh regenerated ({result.stdout.strip()})"


def append_insert_to_db_sh(insert_sql, ref=None, category="directory"):
    """Write the INSERT to per-entity file and regenerate db.sh.

    Legacy-compatible: if ref is None, falls back to appending directly to db.sh.
    """
    if ref is None:
        # Legacy mode: append directly to db.sh
        if not SEED_DB_PATH.exists():
            return False, f"db.sh not found at {SEED_DB_PATH}"
        content = SEED_DB_PATH.read_text()
        commit_marker = "\n  commit;\nEOSQL"
        if commit_marker in content:
            insert_pos = content.rfind(commit_marker)
            new_content = content[:insert_pos] + "\n" + insert_sql + content[insert_pos + 1:]
        else:
            eosql_marker = "\nEOSQL"
            if eosql_marker in content:
                insert_pos = content.rfind(eosql_marker)
                new_content = content[:insert_pos] + "\n" + insert_sql + "\n" + content[insert_pos:]
            else:
                return False, "Could not find insertion point in db.sh"
        SEED_DB_PATH.write_text(new_content)
        return True, "INSERT appended to db.sh (legacy mode)"
    else:
        # New mode: write per-entity file, then regenerate db.sh
        ok, msg = write_system_object_insert(insert_sql, ref, category)
        if not ok:
            return False, msg
        ok, msg = regenerate_db_sh()
        if not ok:
            return False, msg
        return True, f"INSERT written for '{ref}' and db.sh regenerated"
