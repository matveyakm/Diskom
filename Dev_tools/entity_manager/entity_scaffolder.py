def indent(text, spaces=2):
    return "\n".join(" " * spaces + line if line else "" for line in text.splitlines())


def escape_sql(val):
    """Escape a value for SQL: double single quotes, wrap in single quotes."""
    s = str(val).replace("'", "''")
    return f"'{s}'"


def generate_table_columns_json(fields):
    """Generate table_columns JSON from field definitions.
    Skips fields marked exclude_from_table."""
    cols = []
    for f in fields:
        if f.get("exclude_from_table"):
            continue
        col = {
            "field": f.get("column", f["name"]),
            "label": f.get("ui_label", ""),
            "hidden": f.get("hidden", False) or f.get("is_pk", False),
        }
        cols.append(col)
    import json
    return json.dumps({"columns": cols}, ensure_ascii=False, indent=2)


def generate_item_fields_json(fields, category="directory"):
    """Generate item_fields JSON from field definitions.
    Excludes fields marked exclude_from_form.
    Always excludes status (workflow field managed by backend logic)."""
    system_cols = {"status"}
    items = []
    for f in fields:
        col = f.get("column", f["name"])
        if f.get("exclude_from_form") or col in system_cols:
            continue
        item = {
            "field": col,
            "label": f.get("ui_label", ""),
            "hidden": f.get("hidden", False) or f.get("is_pk", False),
            "required": f.get("not_null", False),
        }
        items.append(item)
    import json
    return json.dumps({"fields": items}, ensure_ascii=False, indent=2)


